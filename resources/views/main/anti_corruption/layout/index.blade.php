      <h4 class="text-xl font-bold text-gray-800">1. Tata Laksana Pelayanan</h4>
      <p>
        Tata laksana pelayanan di Kantor Desa Motoling Dua (menggantikan Wiaulapi) diatur sebagai berikut:
      </p>

      <div class="ml-4">
        <h5 class="text-lg font-semibold text-gray-800">a. Waktu Pelayanan</h5>
        <ul class="list-disc list-inside mt-2 space-y-1">
          <li>Senin - Kamis : Pukul 08.00 - 14.00 WITA</li>
          <li>Jumat : Pukul 08.00 - 11.00 WITA</li>
          <li>Sabtu, Minggu, Hari Libur Nasional : Tutup</li>
        </ul>
      </div>

      <div class="ml-4">
        <h5 class="text-lg font-semibold text-gray-800">b. Proses Pelayanan</h5>
        <ol class="list-decimal list-inside mt-2 space-y-2">
          <li>Pemohon datang ke Kantor Hukum Tua (Kepala Desa) dengan membawa berkas/dokumen pendukung yang diperlukan.</li>
          <li>Pemohon menyampaikan keperluannya dan menyerahkan berkas/dokumen pendukung kepada petugas pelayanan.</li>
          <li>Petugas pelayanan memeriksa kelengkapan dan keabsahan berkas/dokumen pendukung.</li>
          <li>Dokumen diproses, divalidasi, dan disahkan oleh Hukum Tua, kemudian diserahkan kepada pemohon.</li>
        </ol>
      </div>

      <h4 class="text-xl font-bold text-gray-800 mt-8">2. Standar Pelayanan</h4>
      <p>
        Pelayanan di kantor desa menganut prinsip-prinsip sebagai berikut:
      </p>
      <ul class="list-disc list-inside mt-2 space-y-1">
        <li><strong>Kesederhanaan:</strong> Prosedur pelayanan tidak berbelit-belit.</li>
        <li><strong>Kejelasan:</strong> Persyaratan dan prosedur pelayanan jelas dan transparan.</li>
        <li><strong>Kepastian Waktu:</strong> Pelayanan dilaksanakan dalam kurun waktu yang telah ditentukan.</li>
        <li><strong>Akurasi:</strong> Produk pelayanan (dokumen) yang dihasilkan akurat dan sah.</li>
        <li><strong>Keamanan:</strong> Proses dan produk pelayanan memberikan keamanan dan kepastian hukum.</li>
        <li><strong>Tanggung Jawab:</strong> Ada unit atau petugas yang bertanggung jawab atas pelayanan.</li>
        <li><strong>Kelengkapan Sarana:</strong> Tersedianya sarana dan prasarana kerja yang memadai.</li>
        <li><strong>Kemudahan Akses:</strong> Lokasi dan sarana pelayanan mudah dijangkau.</li>
        <li><strong>Kedisiplinan:</strong> Petugas bersikap disiplin, sopan, dan santun.</li>
        <li><strong>Kenyamanan:</strong> Lingkungan pelayanan tertib, rapi, bersih, dan nyaman.</li>
      </ul>

      <h4 class="text-xl font-bold text-gray-800 mt-8">3. Kompetensi Petugas</h4>
      <p>
        Petugas pelayanan di kantor desa memiliki kompetensi sebagai berikut:
      </p>
      <ul class="list-disc list-inside mt-2 space-y-1">
        <li>Mampu mengoperasikan komputer dan aplikasi layanan.</li>
        <li>Bersikap ramah, sopan, dan santun kepada pemohon.</li>
        <li>Komunikatif dalam menyampaikan informasi.</li>
        <li>Disiplin dan bertanggung jawab terhadap pekerjaan.</li>
      </ul>

      <h4 class="text-xl font-bold text-gray-800 mt-8">4. Sarana dan Prasarana</h4>
      <p>
        Untuk mendukung kelancaran pelayanan, kantor desa dilengkapi dengan:
      </p>
      <ul class="list-disc list-inside mt-2 space-y-1">
        <li>Gedung kantor desa yang representatif.</li>
        <li>Meja dan kursi pelayanan.</li>
        <li>Perangkat komputer dan printer.</li>
        <li>Jaringan internet.</li>
        <li>Alat Tulis Kantor (ATK).</li>
      </ul>

      <h4 class="text-xl font-bold text-gray-800 mt-8">5. Penanganan Pengaduan</h4>
      <p>
        Mekanisme penanganan pengaduan, kritik, atau masukan dari warga dapat dilakukan melalui:
      </p>
      <ul class="list-disc list-inside mt-2 space-y-1">
        <li>Datang langsung ke kantor desa dan menemui petugas atau pimpinan.</li>
        <li>Melalui telepon, SMS, atau WhatsApp ke nomor layanan desa.</li>
